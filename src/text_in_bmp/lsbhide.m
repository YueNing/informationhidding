%函数功能:本函数将完成在LSB上的顺序信息隐秘，载体选用我自选的BMP图像
%输入格式举例:[ste_cover,len_total]=lsbhide('lenna.bmp','message.txt','LSBcover.bmp')
%参数说明：
%input是信息隐蔽载体图像,为BMP图像
%file是秘密消息文件
%output是信息隐秘后生成图像
%ste_cover是信息隐秘后图像矩阵
%len_total是秘密消息的长度，即容量


function [ste_cover,len_total]=lsbhide(input,file,output)


%读人图像矩阵
cover=imread(input);
ste_cover=cover;
ste_cover=double(ste_cover);%在矩阵中使用的数据类型是double型


%将文本文件转换为二进制序列
f_id=fopen(file,'r');
[msg,len_total]=fread(f_id,'ubit1'); %一次读一位，写入矩阵msg


%判断嵌入消息量是否过大
[m,n]=size(ste_cover);
if len_total>m*n
    error('嵌入消息量过大，请更换图像');
end

%文本的嵌入过程
%p作为消息嵌入位数计数器
p=1;
for f2=1:n %MATLAB按列读取
    for f1=1:m %MATLAB按行读取
        ste_cover(f1,f2)=ste_cover(f1,f2)-mod(ste_cover(f1,f2),2)+msg(p,1);%将文本信息隐藏到图片中
          if p==len_total
             break;
          end
          p=p+1;
    end 
    if p==len_total
        break;
    end
end


%二进制转换为十进制
ste_cover=uint8(ste_cover); %8位无符号整形


%生成信息隐蔽后的图像
imwrite(ste_cover,output);

